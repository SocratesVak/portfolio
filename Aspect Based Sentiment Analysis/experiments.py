from test_1200 import test1200


experiments_k_200 = []
experiments_scores_k_200 = []
experiments_k_1200 = []
experiments_scores_k_1200 = []
k = 10 # number of datasets


"""δημιουργούμε μια δομή for, η οποία είναι σε θέση να μας παραγάγει λίστες ισάριθμες με τον αριθμό των αρχείων(10). 
Κάθε μια λίστα θα περιέχει 9 αριθμούς, όσοι και οι φάκελοι που θα την εκπαιδεύσουμε. Ωστόσο, κάθε λίστα θα αποκλείει κάθε
φορά έναν άλλο αριθμό της ακολουθίας. Έτσι, τεχνικά έχουμε δημιουργήσει τις προϋποθέσεις για να προβούμε σε cross validation,
όπου ο κάθε φάκελος για μια φορά δε θα περιλαμβάνεται στα δεδομένα εκπαίδευσης, αλλά στα άγνωστα για το μοντέλο δεδομένα"""

for x in range(k):
  set = []
  for y in range(k):
    if x!=y:
      set.append(str(y+1))
  train_set = '[' + ','.join(set) + ']'
  train200(train_set)
  k_200 = test200(str(x+1))
  train1200(train_set)
  k_1200 = test1200(str(x+1))
  experiments_k_200.append('For part '+ str(x+1) +' the accuracy score for polarity and aspect respectively with k=200 is:'+ str(k_200))
  experiments_k_1200.append('For part '+ str(x+1) +' the accuracy score for polarity and aspect respectively with k=1200 is:'+ str(k_1200))

  """αποθηκεύουμε τα δεδομένα σε διαφορετικές λίστες, ώστε να μπορούμε να τα αξιοποιήσουμε για την εξαγωγή του mean accuracy"""

  experiments_scores_k_200.append(k_200)
  experiments_scores_k_1200.append(k_1200)

print('\n','-------------------','\n')
